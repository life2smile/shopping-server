public class BottomTabData {
    private String tabName;
    private String tabUnselectImg;
    private String tabSelectImg;

    public String getTabName() {
        return tabName;
    }

    public void setTabName(String tabName) {
        this.tabName = tabName;
    }

    public String getTabUnselectImg() {
        return tabUnselectImg;
    }

    public void setTabUnselectImg(String tabUnselectImg) {
        this.tabUnselectImg = tabUnselectImg;
    }

    public String getTabSelectImg() {
        return tabSelectImg;
    }

    public void setTabSelectImg(String tabSelectImg) {
        this.tabSelectImg = tabSelectImg;
    }
}
